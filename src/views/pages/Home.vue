        }).catch((error) => {
          console.log(error.response.data)
        })
    },
    deletePost(postId) {
      if (confirm("Do you really want to delete?")) {
        axios.delete('/blogs/' + postId, {
          headers: {
            'Content-Type': 'application/json',
            'Authorization': this.$store.getters.GET_AUTH_TOKEN
          },
        })
          .then(res => {
            console.log(res)
